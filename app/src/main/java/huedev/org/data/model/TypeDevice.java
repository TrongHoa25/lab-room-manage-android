package huedev.org.data.model;

import java.io.Serializable;
import android.os.Parcel;
import android.os.Parcelable;
import android.os.Parcelable.Creator;
import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class TypeDevice extends BaseModel implements Serializable, Parcelable
{

    @SerializedName("id")
    @Expose
    private Integer id;
    @SerializedName("name")
    @Expose
    private String name;
    public final static Parcelable.Creator<TypeDevice> CREATOR = new Creator<TypeDevice>() {


        @SuppressWarnings({
                "unchecked"
        })
        public TypeDevice createFromParcel(Parcel in) {
            return new TypeDevice(in);
        }

        public TypeDevice[] newArray(int size) {
            return (new TypeDevice[size]);
        }

    }
            ;
    private final static long serialVersionUID = -4916067911267915474L;

    protected TypeDevice(Parcel in) {
        this.id = ((Integer) in.readValue((Integer.class.getClassLoader())));
        this.name = ((String) in.readValue((String.class.getClassLoader())));
    }

    public TypeDevice() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void writeToParcel(Parcel dest, int flags) {
        dest.writeValue(id);
        dest.writeValue(name);
    }

    public int describeContents() {
        return 0;
    }

}
